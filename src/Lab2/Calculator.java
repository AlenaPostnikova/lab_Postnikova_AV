package Lab2;

public class Calculator {

    static int fnSum(int x, int y) {
        return (x+y);
        }

    static double fnSum(double x, double y) {
        return (x+y);
    }

    static long fnSum(long x, long y) {
        return (x+y);
    }

    static double fnDiv(int x, int y) {
        return (x/(double)y);
    }

    static double fnDiv(double x, double y) {
        return (x/y);
    }

    static double fnDiv(long x, long y) {
        return (x/(double)y);
    }

    static int fnMinus(int x, int y) {
        return (x-y);
    }

    static double fnMinus(double x, double y) {
        return (x-y);
    }

    static long fnMinus(long x, long y) {
        return (x-y);
    }

    static int fnMultiply(int x, int y) {
        return (x*y);
    }

    static double fnMultiply(double x, double y) {
        return (x*y);
    }

    static long fnMultiply(long x, long y) {
        return (x*y);
    }
}


